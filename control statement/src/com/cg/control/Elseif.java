package com.cg.control;

public class Elseif {

	public static void main(String[] args) {
		String lang = "java";
		if(lang == "python") {
			System.out.println("language is python");
			
		}else if(lang == "c++") {
			System.out.println("lang is c++");
			
		}else if(lang =="java") {
			System.out.println("lang is java");
			}else {
				System.out.println(lang);
			}
		}

}
